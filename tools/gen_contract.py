    for item in [
        "2.4.1. Требовать от Исполнителя информации о ходе выполнения работ.",
        "2.4.2. Требовать устранения выявленных дефектов за счёт Исполнителя.",
        "2.4.3. Отказаться от исполнения Договора в случае существенного нарушения его условий Исполнителем.",
    ]:
        add_sub(doc, item)

    # Article 3 - Cost and payment
    add_article(doc, "3", "СТОИМОСТЬ УСЛУГ И ПОРЯДОК ОПЛАТЫ")

